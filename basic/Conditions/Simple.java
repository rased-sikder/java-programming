package Conditions;

public class Simple {

    public static void main (String[] args) {

        int time = 20;
        if (time < 10) {

            System.out.println(time + " is less that 10");

        } else if (time < 5) {
            System.out.println(time + " is less that 5");

        }
        else {
            System.out.println(time + " is greater that 5");

        }

    }


}
